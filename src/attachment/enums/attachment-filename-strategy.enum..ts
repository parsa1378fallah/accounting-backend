//--------------------------------------------------
// Attachment Filename Strategy
//--------------------------------------------------

export enum AttachmentFilenameStrategy {

    //--------------------------------------------------
    // Universally Unique Identifier
    //--------------------------------------------------

    UUID = 'uuid',

    //--------------------------------------------------
    // Timestamp + Random
    //--------------------------------------------------

    TIMESTAMP = 'timestamp',

    //--------------------------------------------------
    // Cryptographically Random
    //--------------------------------------------------

    RANDOM = 'random',

    //--------------------------------------------------
    // SHA-256 Hash
    //--------------------------------------------------

    HASH = 'hash',

}